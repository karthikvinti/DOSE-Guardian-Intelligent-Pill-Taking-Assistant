#define  SETBIT(DATA,BP) DATA=(DATA|(1<<BP))
#define  CLEARBIT(DATA,BP) DATA=(DATA&~(1<<BP))
#define  CLRBIT(DATA,BP) DATA = (1<<BP)
#define  TOGGLEBIT(DATA,BP) DATA=(DATA^(1<<BP))

#define  WRITEBIT(WORD,BP,BIT) WORD=(WORD&~(1<<BP)|(BIT<<BP))
#define  WRITEBYTE(WORD,SBP,BYTE_DATA) WORD=(WORD&~(0xFF<<SBP)|(BYTE_DATA<<SBP))
#define  WRITENIBBLE(WORD,SBP,NIBBLE_DATA) WORD=(WORD&~(0X0F<<SBP)|(NIBBLE_DATA<<SBP))
#define  WRITEHWORD(WORD,SBP,HWORD_DATA) WORD=(WORD&~(0XFFFF<<SBP)|(HWORD<<SBP))
#define  READBIT(WORD,BP) ((WORD>>BP)&1)
